package com.bankofshanghai.mypojo;

/**
 * 金额分布
 * 
 * @author DMM
 *
 */
public class MoneyDistribute {

	private double m0;
	private double m1;
	private double m2;
	private double m3;
	private double m4;

	public MoneyDistribute(double m0, double m1, double m2, double m3, double m4) {
		this.m0 = m0;
		this.m1 = m1;
		this.m2 = m2;
		this.m3 = m3;
		this.m4 = m4;
	}

	public double getM0() {
		return m0;
	}

	public void setM0(double m0) {
		this.m0 = m0;
	}

	public double getM1() {
		return m1;
	}

	public void setM1(double m1) {
		this.m1 = m1;
	}

	public double getM2() {
		return m2;
	}

	public void setM2(double m2) {
		this.m2 = m2;
	}

	public double getM3() {
		return m3;
	}

	public void setM3(double m3) {
		this.m3 = m3;
	}

	public double getM4() {
		return m4;
	}

	public void setM4(double m4) {
		this.m4 = m4;
	}

}
